package com.pratik;

public class basics {
    public static void main(String[] args){
//        if statement
        int i=1;
        if (i==1){
            System.out.println("the number is 1");
        }
//        basic while
        while (i != 5 ){
            System.out.println(i);
            i++;
        }
//        for loops
        for (int count =1; count<=5; count++){
            System.out.println(count);
            i++;
        }
    }
}
